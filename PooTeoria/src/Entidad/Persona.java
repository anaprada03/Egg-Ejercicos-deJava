package Entidad;

public class Persona {
public String Nombre;  
public int edad;
public String genero;
public float altura;
public float peso;

    public Persona() {
    }

    public Persona(String Nombre, int edad, String genero, float altura, float peso) {
        this.Nombre = Nombre;
        this.edad = edad;
        this.genero = genero;
        this.altura = altura;
        this.peso = peso;
    }


    
}
